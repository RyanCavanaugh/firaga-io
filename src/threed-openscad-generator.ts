import { PartListImage } from "./image-utils";
import { colorEntryToHex } from "./utils";

declare const JSZip: any;
declare const saveAs: typeof import("file-saver").saveAs;

export async function generateOpenSCADMasks(image: PartListImage, filename: string) {
    await loadJSZip();
    const zip = new JSZip();
    
    // Create one monochrome image per color
    image.partList.forEach((part, partIdx) => {
        const canvas = document.createElement('canvas');
        canvas.width = image.width;
        canvas.height = image.height;
        const ctx = canvas.getContext('2d')!;
        
        const imageData = ctx.createImageData(image.width, image.height);
        
        for (let y = 0; y < image.height; y++) {
            for (let x = 0; x < image.width; x++) {
                const idx = (y * image.width + x) * 4;
                const isThisColor = image.pixels[y][x] === partIdx;
                const value = isThisColor ? 255 : 0;
                
                imageData.data[idx] = value;     // R
                imageData.data[idx + 1] = value; // G
                imageData.data[idx + 2] = value; // B
                imageData.data[idx + 3] = 255;   // A
            }
        }
        
        ctx.putImageData(imageData, 0, 0);
        
        // Convert canvas to blob and add to zip
        canvas.toBlob((blob) => {
            const safeName = sanitizeFilename(part.target.name);
            zip.file(`${safeName}_${partIdx}.png`, blob);
        });
    });
    
    // Generate the OpenSCAD file
    const scadContent = generateSCADFile(image);
    zip.file(`${filename}.scad`, scadContent);
    
    // Generate and download the zip file
    setTimeout(async () => {
        const content = await zip.generateAsync({ type: 'blob' });
        saveAs(content, `${filename}_openscad.zip`);
    }, 500); // Give canvas.toBlob time to complete
}

function generateSCADFile(image: PartListImage): string {
    let scad = '// OpenSCAD 3D Display\n';
    scad += '// Generated by firaga.io\n\n';
    
    scad += '// Parameters\n';
    scad += `image_width = ${image.width};\n`;
    scad += `image_height = ${image.height};\n`;
    scad += 'pixel_size = 2.5; // mm per pixel\n';
    scad += 'layer_height = 1.0; // mm height per layer\n\n';
    
    scad += '// Create 3D display from heightmap images\n';
    scad += 'module display() {\n';
    
    image.partList.forEach((part, partIdx) => {
        const safeName = sanitizeFilename(part.target.name);
        const hex = colorEntryToHex(part.target);
        
        scad += `    // Layer ${partIdx + 1}: ${part.target.name} (${hex})\n`;
        scad += `    color("${hex}") {\n`;
        scad += `        surface(file = "${safeName}_${partIdx}.png", center = true, invert = false);\n`;
        scad += `        scale([pixel_size / image_width, pixel_size / image_height, layer_height]) {\n`;
        scad += `            translate([-image_width/2, -image_height/2, ${partIdx}]) {\n`;
        scad += `                surface(file = "${safeName}_${partIdx}.png", center = false, invert = false);\n`;
        scad += '            }\n';
        scad += '        }\n';
        scad += '    }\n\n';
    });
    
    scad += '}\n\n';
    scad += 'display();\n';
    
    return scad;
}

function sanitizeFilename(name: string): string {
    return name
        .replace(/[^a-zA-Z0-9_-]/g, '_')
        .replace(/_+/g, '_')
        .toLowerCase();
}

async function loadJSZip(): Promise<void> {
    return new Promise((resolve, reject) => {
        if (typeof JSZip !== 'undefined') {
            resolve();
            return;
        }
        
        const tagName = 'jszip-script-tag';
        const existingScript = document.getElementById(tagName);
        
        if (existingScript) {
            resolve();
            return;
        }
        
        const script = document.createElement('script');
        script.id = tagName;
        script.src = 'https://cdnjs.cloudflare.com/ajax/libs/jszip/3.10.1/jszip.min.js';
        script.onload = () => resolve();
        script.onerror = () => reject(new Error('Failed to load JSZip'));
        document.head.appendChild(script);
    });
}
