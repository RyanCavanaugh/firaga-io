declare const JSZip: any;

export async function makeOpenSCADMasks(image: PartListImage, filename: string) {
    // Load JSZip from CDN if not already loaded
    await loadJSZip();
    
    const zip = new JSZip();
    
    // Generate mask images for each color
    for (let i = 0; i < image.partList.length; i++) {
        const part = image.partList[i];
        const maskData = generateMaskImage(image, i);
        zip.file(`mask_${i}_${sanitizeFilename(part.target.name)}.png`, maskData.split(',')[1], { base64: true });
    }
    
    // Generate OpenSCAD file
    const scadContent = generateOpenSCADFile(image);
    zip.file(`${filename}.scad`, scadContent);
    
    // Generate the zip file
    const content = await zip.generateAsync({ type: 'blob' });
    downloadBlob(content, `${filename}_openscad.zip`);
}

async function loadJSZip() {
    const tagName = "jszip-script-tag";
    const scriptEl = document.getElementById(tagName);
    
    if (scriptEl === null) {
        return new Promise<void>((resolve) => {
            const tag = document.createElement("script");
            tag.id = tagName;
            tag.onload = () => resolve();
            tag.src = "https://cdnjs.cloudflare.com/ajax/libs/jszip/3.10.1/jszip.min.js";
            document.head.appendChild(tag);
        });
    }
}

function downloadBlob(blob: Blob, filename: string) {
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = filename;
    document.body.appendChild(a);
    a.click();
    document.body.removeChild(a);
    URL.revokeObjectURL(url);
}

function generateMaskImage(image: PartListImage, colorIndex: number): string {
    // Create a canvas for the mask
    const canvas = document.createElement('canvas');
    canvas.width = image.width;
    canvas.height = image.height;
    const ctx = canvas.getContext('2d');
    
    if (!ctx) {
        throw new Error('Could not get canvas context');
    }
    
    // Fill with white background
    ctx.fillStyle = 'white';
    ctx.fillRect(0, 0, image.width, image.height);
    
    // Draw black pixels where this color appears
    ctx.fillStyle = 'black';
    for (let y = 0; y < image.height; y++) {
        for (let x = 0; x < image.width; x++) {
            if (image.pixels[y][x] === colorIndex) {
                ctx.fillRect(x, y, 1, 1);
            }
        }
    }
    
    return canvas.toDataURL('image/png');
}

function generateOpenSCADFile(image: PartListImage): string {
    let scad = `// OpenSCAD file for ${image.width}x${image.height} pixel art
// Generated by firaga.io

// Parameters
pixel_size = 2.5; // mm per pixel
base_height = 0.5; // mm
layer_height = 0.5; // mm per color layer

// Image dimensions
width = ${image.width};
height = ${image.height};

`;

    // Add color definitions
    scad += `// Color definitions\n`;
    image.partList.forEach((part, index) => {
        const color = part.target;
        const rgb = [color.r / 255, color.g / 255, color.b / 255];
        scad += `color_${index} = [${rgb[0].toFixed(3)}, ${rgb[1].toFixed(3)}, ${rgb[2].toFixed(3)}]; // ${color.name}\n`;
    });
    scad += '\n';

    // Add module to create a layer from a heightmap
    scad += `// Module to create a layer from a mask image
module image_layer(image_file, layer_color, z_offset) {
    color(layer_color)
    translate([0, 0, z_offset])
    scale([pixel_size, pixel_size, layer_height])
    surface(file = image_file, center = false, invert = true);
}

`;

    // Main assembly
    scad += `// Main assembly\n`;
    scad += `union() {\n`;
    scad += `    // Base plate\n`;
    scad += `    color([0.8, 0.8, 0.8])\n`;
    scad += `    cube([width * pixel_size, height * pixel_size, base_height]);\n`;
    scad += `    \n`;
    scad += `    // Color layers\n`;
    
    image.partList.forEach((part, index) => {
        const colorName = sanitizeFilename(part.target.name);
        scad += `    image_layer("mask_${index}_${colorName}.png", color_${index}, base_height + ${index} * layer_height);\n`;
    });
    
    scad += `}\n`;

    return scad;
}

function sanitizeFilename(name: string): string {
    return name.replace(/[^a-zA-Z0-9_-]/g, '_');
}
